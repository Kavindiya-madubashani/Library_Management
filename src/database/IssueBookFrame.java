    private void IssueBook_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IssueBook_btnActionPerformed
        String bookId = BookId_txtField.getText().trim();
        String studentId = StudentId_txtField.getText().trim();
        String issueDate = IssueDate_txtField.getText().trim();
        String dueDate = DueDate_txtField.getText().trim();
        
        if(bookId.isEmpty() || studentId.isEmpty() || issueDate.isEmpty() || dueDate.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill all fields!", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            //check if book exists and is available
            String checkBookSql = "SELECT available_quantity FROM books WHERE book_id = ?";
            PreparedStatement checkBook = jdbc.getConnection().prepareStatement(checkBookSql);
            checkBook.setString(1, bookId);
            ResultSet bookResult = checkBook.executeQuery();
            
            if(!bookResult.next()){
                JOptionPane.showMessageDialog(this, "Book ID not found!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int availableQty = bookResult.getInt("available_quantity");
            if(availableQty <= 0){
                JOptionPane.showMessageDialog(this,"Book is not available!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //check if student exists
            String checkStudentSql = "SELECT * FROM members WHERE member_id = ?";
            PreparedStatement checkStudent = jdbc.getConnection().prepareStatement(checkStudentSql);
            checkStudent.setString(1,studentId);
            ResultSet StudentResult = checkStudent.executeQuery();
            
            if(!StudentResult.next()){
                JOptionPane.showMessageDialog(this, "Student ID not found!", "Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //insert borrowing record
            String insertSql = "INSERT INTO borrowings(member_id, book_id,issue_date, due_date) VALUES(?, ?, ?, ?)";
            PreparedStatement insertStmt = jdbc.getConnection().prepareStatement(insertSql);
            insertStmt.setString(1, studentId);
            insertStmt.setString(2, bookId);
            insertStmt.setString(3, issueDate);
            insertStmt.setString(4, dueDate);
            insertStmt.executeUpdate();
            
            //update book available quantity
            String UpdateSql = "UPDATE books SET available_quantity = available_quantity - 1 WHERE book_id = ?";
            PreparedStatement UpdateStmt = jdbc.getConnection().prepareStatement(UpdateSql);
            UpdateStmt.setString(1,bookId);
            UpdateStmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Book issued successfully!", "Success",JOptionPane.INFORMATION_MESSAGE );
            
            //Clear fields
            BookId_txtField.setText("");
            StudentId_txtField.setText("");
            IssueDate_txtField.setText("");
            DueDate_txtField.setText("");  
        
        }catch (Exception e){
                JOptionPane.showMessageDialog(this, "Error" + e.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            
        
        }
    }//GEN-LAST:event_IssueBook_btnActionPerformed

