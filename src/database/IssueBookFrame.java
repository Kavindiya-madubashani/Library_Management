    private void showDatePicker(String fieldType){
        
        //creates a date spinner(shows tdy's date by default)
        JSpinner spinner = new JSpinner(new SpinnerDateModel());
        
        //format to show only date(Mysql's format y/m/d)
        JSpinner.DateEditor dateEditor = new JSpinner.DateEditor(spinner,"yyyy-MM-dd");
        spinner.setEditor(dateEditor);
        
        //show dialog with the spinner
        int result = JOptionPane.showConfirmDialog(this,spinner,
                "select " + fieldType + "Date",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE);
        
        //if user clicked ok, set the text field
        if(result == JOptionPane.OK_OPTION){
            java.util.Date selectedDate = (java.util.Date) spinner.getValue();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate = sdf.format(selectedDate);
            
            if(fieldType.equals("Issue")){
                IssueDate_txtField.setText(formattedDate);
            }else{
                DueDate_txtField.setText(formattedDate);
            }
        }
        
    
    }

