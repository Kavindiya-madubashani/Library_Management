    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
    String id = enterid.getText();
    String t = title.getText();
    String a = author.getText();
    String i = isbn.getText();
    String total = totalQua.getText();
    String avail = availableQua.getText();

    if(id.isEmpty()){
        JOptionPane.showMessageDialog(this, "Enter Book ID to Update");
        return;
    }

    try {
        String sql = "UPDATE books SET title=?, author=?, isbn=?, total_quantity=?, available_quantity=? WHERE book_id=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, t);
        pst.setString(2, a);
        pst.setString(3, i);
        pst.setString(4, total);
        pst.setString(5, avail);
        pst.setString(6, id);

        int n = pst.executeUpdate();  // ✅ Execute only once
        if(n > 0){
        JOptionPane.showMessageDialog(this, "Book Updated Successfully");
        clearFields();
        loadALLbook();  // Refresh table
    }
        

    } catch(SQLException e){
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    


    }//GEN-LAST:event_UpdateActionPerformed

    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
    String id = enterid.getText();
    String t = title.getText();
    String a = author.getText();
    String i = isbn.getText();
    String total = totalQua.getText();
    String avail = availableQua.getText();

    if(id.isEmpty() || t.isEmpty() || a.isEmpty() || i.isEmpty() || total.isEmpty() || avail.isEmpty()){
        JOptionPane.showMessageDialog(this, "Please fill all fields");
        return;
    }
    

    try {
        String sql = "INSERT INTO books(book_id, title, author, isbn, total_quantity, available_quantity) VALUES(?,?,?,?,?,?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, id);
        pst.setString(2, t);
        pst.setString(3, a);
        pst.setString(4, i);
        pst.setString(5, total);
        pst.setString(6, avail);

        int n = pst.executeUpdate();  // ✅ Execute only once
        if(n > 0){
        JOptionPane.showMessageDialog(this, "Book Added Successfully");
        clearFields();
        loadALLbook();  // Refresh table
    }
        
    } catch(SQLException e){
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    
    }//GEN-LAST:event_AddActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
    String id = enterid.getText();

    if(id.isEmpty()){
        JOptionPane.showMessageDialog(this, "Enter Book ID to Delete");
        return;
    }

    try {
        String sql = "DELETE FROM books WHERE book_id=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, id);

        int n = pst.executeUpdate(); // ✅ Execute only once
    if(n > 0){
        JOptionPane.showMessageDialog(this, "Book Deleted Successfully");
        clearFields();
        loadALLbook();  // Refresh table
    }
    
    } catch(SQLException e){
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    
    

    }//GEN-LAST:event_DeleteActionPerformed

