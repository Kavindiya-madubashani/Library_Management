    private void enteridKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_enteridKeyPressed
    String id = enterid.getText();
    if(id.isEmpty()) return;

    try {
        String sql = "SELECT * FROM books WHERE book_id=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, id);
        ResultSet rs = pst.executeQuery();

        if(rs.next()){
            title.setText(rs.getString("title"));
            author.setText(rs.getString("author"));
            isbn.setText(rs.getString("isbn"));
            totalQua.setText(rs.getString("total_quantity"));
            availableQua.setText(rs.getString("available_quantity"));
        } else {
            clearFields();
        }

    } catch(SQLException e){
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

    }//GEN-LAST:event_enteridKeyPressed

